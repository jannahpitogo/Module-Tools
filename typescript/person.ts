export class Person {
    name: string;
    surname: string; 
    age: number;
    address: string; 
    gender: string; 

    constructor (name:string, surname:string, age:number, address:string, gender:string){
        this.name = name; 
        this.surname = surname;  
        this.age = age;
        this.address = address;
        this.gender = gender;
    }
}


